<script lang="ts">
  import { Link } from "svelte-routing";
  import type { LinkDto } from "@/application/dtos/core/links/LinkDto";
  import ButtonTertiary from "@/components/ui/buttons/ButtonTertiary.svelte";
  import EmptyState from "@/components/ui/emptyState/EmptyState.svelte";
  import DateUtils from "@/utils/shared/DateUtils";
  import classNames from "@/utils/shared/ClassesUtils";
  import { _ as t } from "svelte-i18n";

  export let items: LinkDto[];

  let sortByColumn: string = "";
  let sortDirection: number = -1;
  const headers = [
    {
      name: "",
      title: $t("models.client.object"),
    },
    {
      title: $t("models.contract.plural"),
    },
    {
      title: $t("shared.actions"),
    },
  ];

  function sortBy(column: string) {
    if (column) {
      sortDirection = sortDirection === -1 ? 1 : -1;
      sortByColumn = column;
    }
  }
  function dateMonthName(value: Date | undefined) {
    return DateUtils.dateMonthName(value);
  }
  $: sortedItems = (): LinkDto[] => {
    if (!items) {
      return [];
    }
    const column = sortByColumn;
    if (!column || column === "") {
      return items;
    }
    return items.slice().sort((x, y) => {
      if (x[column] && y[column]) {
        if (sortDirection === -1) {
          return (x[column] > y[column] ? 1 : -1) ?? 1;
        } else {
          return (x[column] < y[column] ? 1 : -1) ?? 1;
        }
      }
      return 1;
    });
  };
</script>

<div>
  {#if items.length === 0}
    <div>
      <EmptyState
        className="bg-white"
        captions={{
          thereAreNo: $t("app.clients.empty"),
        }}
      />
    </div>
  {:else}
    <div>
      <div class="sm:hidden">
        <div class="bg-white shadow overflow-hidden sm:rounded-md">
          <ul class="divide-y divide-gray-200">
            {#each sortedItems() as item}
              <li>
                <Link
                  to={"/app/link/" + item.id}
                  class="block hover:bg-gray-50"
                >
                  <div class="flex items-center px-4 py-4 sm:px-6">
                    <div class="min-w-0 flex-1 flex items-center">
                      <div
                        class="min-w-0 flex-1 px-4 md:grid md:grid-cols-2 md:gap-4"
                      >
                        <div>
                          <div class="flex items-end justify-between">
                            <div
                              class="text-sm truncate font-bold text-gray-900 flex-grow"
                            >
                              {item.clientWorkspace.name}
                            </div>
                          </div>
                        </div>
                        <div class="hidden md:block">
                          <div>
                            <p class="text-sm text-gray-900">
                              {#if item.createdAt}
                                <time>{dateMonthName(item.createdAt)}</time
                                >{/if}
                            </p>
                            <p
                              class="mt-2 flex items-center text-sm text-gray-500"
                            >
                              <!--Heroicon name: solid/check-circle -->
                              <svg
                                class="flex-shrink-0 mr-1.5 h-5 w-5 text-green-400"
                                xmlns="http://www.w3.org/2000/svg"
                                viewBox="0 0 20 20"
                                fill="currentColor"
                                aria-hidden="true"
                              >
                                <path
                                  fill-rule="evenodd"
                                  d="M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z"
                                  clip-rule="evenodd"
                                />
                              </svg>
                            </p>
                          </div>
                        </div>
                      </div>
                    </div>
                    <div>
                      <!--Heroicon name: solid/chevron-right -->
                      <svg
                        class="h-5 w-5 text-gray-400"
                        xmlns="http://www.w3.org/2000/svg"
                        viewBox="0 0 20 20"
                        fill="currentColor"
                        aria-hidden="true"
                      >
                        <path
                          fill-rule="evenodd"
                          d="M7.293 14.707a1 1 0 010-1.414L10.586 10 7.293 6.707a1 1 0 011.414-1.414l4 4a1 1 0 010 1.414l-4 4a1 1 0 01-1.414 0z"
                          clip-rule="evenodd"
                        />
                      </svg>
                    </div>
                  </div>
                </Link>
              </li>
            {/each}
          </ul>
        </div>
      </div>
      <div class="hidden sm:block">
        <div class="flex flex-col">
          <div class="overflow-x-auto">
            <div class="py-2 align-middle inline-block min-w-full">
              <div
                class="shadow overflow-hidden border border-gray-200 sm:rounded-lg"
              >
                <table class="min-w-full divide-y divide-gray-200">
                  <thead class="bg-gray-50">
                    <tr>
                      {#each headers as header}
                        <th
                          on:click={() => sortBy(header.name)}
                          scope="col"
                          class={classNames(
                            "px-3 py-2 text-left text-xs font-medium text-gray-500 tracking-wider select-none",
                            header.name && "cursor-pointer hover:text-gray-700"
                          )}
                        >
                          <div
                            class="flex items-center space-x-1 text-gray-500"
                          >
                            <div>{header.title}</div>
                            <div
                              class={classNames(
                                (!header.name ||
                                  sortByColumn !== header.name) &&
                                  "invisible"
                              )}
                            >
                              {#if sortDirection === -1}
                                <svg
                                  xmlns="http://www.w3.org/2000/svg"
                                  class="h-5 w-5"
                                  viewBox="0 0 20 20"
                                  fill="currentColor"
                                >
                                  <path
                                    fill-rule="evenodd"
                                    d="M14.707 12.707a1 1 0 01-1.414 0L10 9.414l-3.293 3.293a1 1 0 01-1.414-1.414l4-4a1 1 0 011.414 0l4 4a1 1 0 010 1.414z"
                                    clip-rule="evenodd"
                                  />
                                </svg>
                              {:else}
                                <svg
                                  xmlns="http://www.w3.org/2000/svg"
                                  class="h-5 w-5"
                                  viewBox="0 0 20 20"
                                  fill="currentColor"
                                >
                                  <path
                                    fill-rule="evenodd"
                                    d="M5.293 7.293a1 1 0 011.414 0L10 10.586l3.293-3.293a1 1 0 111.414 1.414l-4 4a1 1 0 01-1.414 0l-4-4a1 1 0 010-1.414z"
                                    clip-rule="evenodd"
                                  />
                                </svg>
                              {/if}
                            </div>
                          </div>
                        </th>
                      {/each}
                    </tr>
                  </thead>
                  <tbody class="bg-white divide-y divide-gray-200">
                    {#each sortedItems() as item}
                      <tr>
                        <td class="px-3 py-2 whitespace-nowrap">
                          <div class="text-sm font-extrabold text-gray-900">
                            {item.clientWorkspace.name}
                          </div>
                        </td>
                        <td
                          class="px-3 py-2 whitespace-nowrap text-sm text-gray-600"
                        >
                          {#if item.contracts && item.contracts.length === 0}
                            <span class="italic text-gray-400"
                              >{$t("shared.notApplicable")}</span
                            >
                          {:else}
                            <Link
                              to={"/app/contracts/all?l=" + item.id}
                              class="text-theme-600 hover:text-theme-800 lowercase underline font-medium"
                            >
                              {#if item.contracts}
                                <span>
                                  {item.contracts.length}
                                  {#if item.contracts.length === 1}
                                    <span>{$t("models.contract.object")}</span>
                                  {:else}
                                    <span>{$t("models.contract.plural")}</span>
                                  {/if}
                                </span>
                              {/if}
                            </Link>
                          {/if}
                        </td>

                        <td
                          class="w-20 px-3 py-2 whitespace-nowrap text-sm text-gray-600"
                        >
                          <div class="flex items-center space-x-2">
                            <ButtonTertiary
                              to={"/app/contract/new?l=" + item.id}
                            >
                              <div>{$t("app.contracts.new.title")}</div>
                            </ButtonTertiary>
                          </div>
                        </td>
                      </tr>
                    {/each}
                  </tbody>
                </table>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  {/if}
</div>
